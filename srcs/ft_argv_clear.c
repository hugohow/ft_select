#include "ft_select.h"

void ft_argv_clear(void)
{
	int i;

	i = 0;
	while (i < E.line + 1)
	{
		ft_dprintf(0 ,"\033[K");
		ft_dprintf(0 ,"\033[A");
		i++;
	}
}